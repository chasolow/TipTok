# URL –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏—è
image_url = "https://i.postimg.cc/vZmHCG8k/big.png"

# –ó–∞–≥—Ä—É–∑–∫–∞ –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏—è
