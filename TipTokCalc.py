# –ó–∞–≥–æ–ª–æ–≤–æ–∫
st.markdown("<h1 style='text-align: center;'>–†–∞—Å—Å—á–µ—Ç —Å—Ç–æ–∏–º–æ—Å—Ç–∏ —É—Å–ª—É–≥</h1>", unsafe_allow_html=True)

# –í–æ–ø—Ä–æ—Å –æ –º–æ—â–Ω–æ—Å—Ç–∏ —Å –Ω–æ–≤—ã–º–∏ –≤–∞—Ä–∏–∞–Ω—Ç–∞–º–∏
power_question = st.radio("–ï—Å—Ç—å –ª–∏ –Ω–∞ –æ–±—ä–µ–∫—Ç–µ —Å—É—â–µ—Å—Ç–≤—É—é—â–∞—è –º–æ—â–Ω–æ—Å—Ç—å —Å–æ–≥–ª–∞—Å–Ω–æ —Ç–µ—Ö–Ω–∏—á–µ—Å–∫–∏–º —É—Å–ª–æ–≤–∏—è–º?", ['‚ö°Ô∏è –î–∞', '‚ùå –ù–µ—Ç'])

# –ü–æ–ª—è –¥–ª—è –≤–≤–æ–¥–∞ –º–æ—â–Ω–æ—Å—Ç–∏ –≤ –æ–¥–Ω—É —Å—Ç—Ä–æ–∫—É
col1, col2 = st.columns(2)
if power_question == '‚ö°Ô∏è –î–∞':
    with col1:
        P = st.number_input("–í–≤–µ–¥–∏—Ç–µ —Å—É–º–º–∞—Ä–Ω—É—é –º–æ—â–Ω–æ—Å—Ç—å –æ–±—ä–µ–∫—Ç–∞ (P, –∫–í—Ç):", min_value=0, max_value=500000, step=0.001, value=0)
    with col2:
        Pdop = st.number_input("–í–≤–µ–¥–∏—Ç–µ –¥–æ–ø–æ–ª–Ω–∏—Ç–µ–ª—å–Ω—É—é –º–æ—â–Ω–æ—Å—Ç—å (P–¥–æ–ø, –∫–í—Ç):", min_value=0, max_value=500000, step=0.001, value=0)
else:
    with col1:
        P = st.number_input("–í–≤–µ–¥–∏—Ç–µ —Å—É–º–º–∞—Ä–Ω—É—é –º–æ—â–Ω–æ—Å—Ç—å –æ–±—ä–µ–∫—Ç–∞ (P, –∫–í—Ç):", min_value=0, max_value=500000, step=0.001, value=0)
    Pdop = None

# –ü—Ä–µ–æ–±—Ä–∞–∑—É–µ–º —Ç–µ–∫—Å—Ç –≤ —á–∏—Å–ª–∞ –¥–ª—è —Ä–∞—Å—á–µ—Ç–æ–≤
P = float(P)
Pdop = float(Pdop) if Pdop else None

# –í—ã–±–æ—Ä –∫–ª–∞—Å—Å–∞ –Ω–∞–ø—Ä—è–∂–µ–Ω–∏—è —Å –ø–æ–º–æ—â—å—é radio
Ku = st.radio("–ö–ª–∞—Å—Å –Ω–∞–ø—Ä—è–∂–µ–Ω–∏—è –≤ —Ç–æ—á–∫–µ –ø—Ä–∏—Å–æ–µ–¥–∏–Ω–µ–Ω–∏—è:", [("–î–æ 1000 –í", 1), ("3 - 35 –∫–í", 1.1), ("110 –∫–í", 1.2), ("220 –∫–í", 1.3)])

# –í–æ–ø—Ä–æ—Å –æ –∫–æ–º–ø–µ–Ω—Å–∞—Ü–∏–∏ —Ä–µ–∞–∫—Ç–∏–≤–Ω–æ–π –º–æ—â–Ω–æ—Å—Ç–∏ —Å –Ω–æ–≤—ã–º–∏ –≤–∞—Ä–∏–∞–Ω—Ç–∞–º–∏
Ktg = st.radio("–ï—Å—Ç—å –ª–∏ –≤ —Ç–µ—Ö–Ω–∏—á–µ—Å–∫–∏—Ö —É—Å–ª–æ–≤–∏—è—Ö –ø—É–Ω–∫—Ç –ø–æ –∫–æ–º–ø–µ–Ω—Å–∞—Ü–∏–∏ —Ä–µ–∞–∫—Ç–∏–≤–Ω–æ–π –º–æ—â–Ω–æ—Å—Ç–∏?", ['‚úÖ –ï—Å—Ç—å', '‚ùå –ù–µ—Ç'])

# –í–æ–ø—Ä–æ—Å –æ —Å—Ö–µ–º–∞—Ö –ø–∏—Ç–∞–Ω–∏—è —Å –Ω–æ–≤—ã–º–∏ –≤–∞—Ä–∏–∞–Ω—Ç–∞–º–∏
schemes = st.radio("–ï—Å—Ç—å –ª–∏ —Å—Ö–µ–º—ã –ø–∏—Ç–∞–Ω–∏—è —ç–ª–µ–∫—Ç—Ä–æ–ø—Ä–∏–µ–º–Ω–∏–∫–æ–≤ –Ω–∞ –æ–±—ä–µ–∫—Ç–µ?", ['‚úÖ –ï—Å—Ç—å', '‚ùå –ù–µ—Ç'])

# –ü–æ–ª—è –¥–ª—è –≤–≤–æ–¥–∞ —Å—Ö–µ–º
if schemes == '‚úÖ –ï—Å—Ç—å':
    col3, col4 = st.columns(2)
    with col3:
        X = st.number_input("–ö–æ–ª–∏—á–µ—Å—Ç–≤–æ —É—á–∞—Å—Ç–∫–æ–≤ –õ–≠–ü –æ—Ç —Ü–µ–Ω—Ç—Ä–∞ –ø–∏—Ç–∞–Ω–∏—è –¥–æ –∫–∞–∂–¥–æ–≥–æ —ç–ª–µ–∫—Ç—Ä–æ–ø—Ä–∏–µ–º–Ω–∏–∫–∞:", min_value=0, max_value=5000, step=1, value=0)
    with col4:
        Y = st.number_input("–ö–æ–ª–∏—á–µ—Å—Ç–≤–æ —ç–ª–µ–∫—Ç—Ä–æ–ø—Ä–∏–µ–º–Ω–∏–∫–æ–≤:", min_value=0, max_value=5000, step=1, value=0)
else:
    Y = st.number_input("–ö–æ–ª–∏—á–µ—Å—Ç–≤–æ —ç–ª–µ–∫—Ç—Ä–æ–ø—Ä–∏–µ–º–Ω–∏–∫–æ–≤:", min_value=0, max_value=5000, step=1, value=0)
    X = Y * 1.05

# –í–æ–ø—Ä–æ—Å –æ —Å–æ–ø—Ä–æ–≤–æ–∂–¥–µ–Ω–∏–∏ —Å–æ–≥–ª–∞—Å–æ–≤–∞–Ω–∏—è —Å –Ω–æ–≤—ã–º–∏ –≤–∞—Ä–∏–∞–Ω—Ç–∞–º–∏
Kc = st.radio("–¢—Ä–µ–±—É–µ—Ç—Å—è –ª–∏ —Å–æ–ø—Ä–æ–≤–æ–∂–¥–µ–Ω–∏–µ —Å–æ–≥–ª–∞—Å–æ–≤–∞–Ω–∏—è –†–î –≤ —ç–ª–µ–∫—Ç—Ä–∏—á–µ—Å–∫–∏—Ö —Å–µ—Ç—è—Ö?", ['üìù –¢—Ä–µ–±—É–µ—Ç—Å—è', '‚ùå –ù–µ —Ç—Ä–µ–±—É–µ—Ç—Å—è'])

# –ö–Ω–æ–ø–∫–∞ —Ä–∞—Å—á–µ—Ç–∞
if st.button('–†–ê–°–°–ß–Å–¢'):
    try:
        if P <= 0:
            st.error("–ü–∞—Ä–∞–º–µ—Ç—Ä—ã –¥–æ–ª–∂–Ω—ã –±—ã—Ç—å –±–æ–ª—å—à–µ –Ω—É–ª—è")
        else:
            # –†–∞—Å—Å—á–∏—Ç—ã–≤–∞–µ–º –∫–æ—ç—Ñ—Ñ–∏—Ü–∏–µ–Ω—Ç—ã
            Kp = (0.8533 * Pdop ** 0.0599 + (0.8533 * P ** 0.0599 - 0.8533 * Pdop ** 0.0599) * Pdop / P) if Pdop else 0.8533 * P ** 0.0599
            Ku_value = Ku[1]
            Ktg_value = 1.1 if Ktg == '‚úÖ –ï—Å—Ç—å' else 1
            Kc_value = 1.05 if Kc == 'üìù –¢—Ä–µ–±—É–µ—Ç—Å—è' else 1

            if schemes == '‚úÖ –ï—Å—Ç—å':
                Gx = 1892.9 * X ** -0.544
                Gy = 379.89 * Y ** -0.271
                Gz = 0  # Gz –Ω–µ —Ä–∞—Å—Å—á–∏—Ç—ã–≤–∞–µ–º
            else:
                Gx = 1892.9 * X ** -0.544
                Gy = 379.89 * Y ** -0.271
                Gz = 966.81 * 2 * X ** -0.424

            # –û–±—â–∞—è —Å—Ç–æ–∏–º–æ—Å—Ç—å
            cost = round(((X * Gx + Y * Gy) * Kp * Ku_value * Ktg_value * Kc_value + X * Gz) / 100) * 100
            st.success(f"–û–±—â–∞—è —Å—Ç–æ–∏–º–æ—Å—Ç—å: {cost} —Ä—É–±–ª–µ–π")

    except ZeroDivisionError:
        st.error("–û—à–∏–±–∫–∞: –¥–µ–ª–µ–Ω–∏–µ –Ω–∞ –Ω–æ–ª—å –Ω–µ–≤–æ–∑–º–æ–∂–Ω–æ.")
    except ValueError as e:
        st.error(f"–û—à–∏–±–∫–∞: {e}")
